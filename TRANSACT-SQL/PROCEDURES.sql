CREATE PROCEDURE [dbo].[PCDINSERTAR_MENU] (@strNombre VARCHAR(200), @strUbicacion VARCHAR(500), @intIdPadre INTEGER)
AS
BEGIN 
	DECLARE @intId INT;
	BEGIN TRY
		BEGIN TRANSACTION 
			SELECT @intId  = COUNT(ID) FROM TBLMENU WHERE TRIM(UPPER(UBICACION)) = TRIM(UPPER(@strUbicacion));
			IF @intId > 0 
			BEGIN
				RAISERROR('Se encontró otro MENÚ asociado a la URL ingresada.', 11, 1);
			END			
			INSERT INTO TBLMENU(ID, NOMBREMENU, UBICACION, POSICION, IDPADRE)
			VALUES((SELECT CASE WHEN MAX(ID) IS NULL THEN 1 ELSE MAX(ID)+1 END FROM TBLMENU), @strNombre, @strUbicacion,						
			(SELECT CASE WHEN @intIdPadre = 0 THEN
							(SELECT CASE WHEN MAX(POSICION) IS NULL THEN 1 
									ELSE MAX(POSICION)+1 END FROM TBLMENU WHERE ID = IDPADRE) 
				ELSE (SELECT CASE WHEN MAX(POSICION) IS NULL THEN 1 ELSE MAX(POSICION)+1 END FROM TBLMENU WHERE IDPADRE = @intIdPadre AND ID <> @intIdPadre) 
			END),
			(SELECT CASE WHEN @intIdPadre = 0 THEN 
						(SELECT CASE WHEN MAX(ID) IS NULL THEN 1 
					ELSE MAX(ID)+1 END FROM TBLMENU) ELSE @intIdPadre END))
			COMMIT TRANSACTION;
	 END TRY
	 BEGIN CATCH
		IF @@TRANCOUNT > 0
		BEGIN
			ROLLBACK TRANSACTION;
		END 
		DECLARE @Message varchar(MAX) = ERROR_MESSAGE(),
			@Severity int = ERROR_SEVERITY(),
			@State smallint = ERROR_STATE();
		RAISERROR (@Message, @Severity, @State);
	 END CATCH
END;
GO
